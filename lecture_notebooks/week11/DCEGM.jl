# Discrete Choice EGM

## Intro

* In economics we have often situations that involved *both* a discrete and a continuous choices:
  - Whether to buy a house and how much to save
  - Whether own a car and how much to drive
  - Whether to invest into a certain asset and how much to consume
  - Whether to work 20, 40 or 60 hours per week and how much to consume

* Those are difficult problems to solve because the discrete choice introduces a non-concativity into the value function - a *kink*.
* Today we will introduce a method which efficiently and accurately handles those situations."

